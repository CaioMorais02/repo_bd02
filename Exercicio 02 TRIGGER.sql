CREATE OR ALTER TRIGGER TG_ASSEGURA_SALARIO
ON TB_FUNCIONARIO
AFTER INSERT, UPDATE
AS
BEGIN
	DECLARE @CD_CARGO INT, @CD_ESCOLARIDADE INT, @SALARIO NUMERIC(10 ,2)

	SET @CD_CARGO = (SELECT CD_CARGO FROM INSERTED)
	SET @CD_ESCOLARIDADE = (SELECT CD_ESCOLARIDADE FROM INSERTED)
	SET @SALARIO = (SELECT SALARIO FROM INSERTED)

	-- ANALISTA NÍVEL MÉDIO
	IF (@CD_CARGO = 1 AND @CD_ESCOLARIDADE = 1) AND (@SALARIO < 900.0 OR @SALARIO > 1100.0)
		ROLLBACK
	
	-- ANALISTA NÍVEL SUPERIOR
	ELSE IF (@CD_CARGO = 1 AND @CD_ESCOLARIDADE = 2) AND (@SALARIO < 1200.0 OR @SALARIO > 1300.0)
		ROLLBACK

	-- ANALISTA NÍVEL PÓS-GRAD
	ELSE IF (@CD_CARGO = 1 AND @CD_ESCOLARIDADE = 3) AND (@SALARIO < 1400.0 OR @SALARIO > 1500.0)
		ROLLBACK

	-- DBA NÍVEL MÉDIO
	ELSE IF (@CD_CARGO = 2 AND @CD_ESCOLARIDADE = 1) AND (@SALARIO < 600.0 OR @SALARIO > 700.0)
		ROLLBACK

	-- DBA NÍVEL SUPERIOR
	ELSE IF (@CD_CARGO = 2 AND @CD_ESCOLARIDADE = 2) AND (@SALARIO < 800.0 OR @SALARIO > 1000.0)
		ROLLBACK

	-- DBA NÍVEL PÓS-GRAD
	ELSE IF (@CD_CARGO = 2 AND @CD_ESCOLARIDADE = 3) AND (@SALARIO < 1100.0 OR @SALARIO > 1500.0)
		ROLLBACK

	-- PROGRAMADOR NÍVEL MÉDIO
	ELSE IF (@CD_CARGO = 3 AND @CD_ESCOLARIDADE = 1) AND (@SALARIO < 1300.0 OR @SALARIO > 1500.0)
		ROLLBACK

	-- PROGRAMADOR NÍVEL SUPERIOR
	ELSE IF (@CD_CARGO = 3 AND @CD_ESCOLARIDADE = 2) AND (@SALARIO < 2000.0 OR @SALARIO > 2500.0)
		ROLLBACK

	-- PROGRAMADOR NÍVEL SUPERIOR
	ELSE IF (@CD_CARGO = 3 AND @CD_ESCOLARIDADE = 3) AND (@SALARIO < 3000.0 OR @SALARIO > 3500.0)
		ROLLBACK
END

